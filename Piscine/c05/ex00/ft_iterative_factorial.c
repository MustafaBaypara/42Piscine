int	ft_iterative_factorial(int nb)
{
	int	result;

	result = 1;
	while (nb > 0)
	{
		result *= nb;
		nb--;
	}
	if (nb < 0)
	{
		return (0);
	}
	return (result);
}
#include <stdio.h>
int main (void)
{
	printf("%i", ft_iterative_factorial(3));
}